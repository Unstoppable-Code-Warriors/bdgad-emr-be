export function getRandomElement<T>(array: T[]): T {
  return array[Math.floor(Math.random() * array.length)];
}

export function getRandomElements<T>(array: T[], count: number): T[] {
  const shuffled = [...array].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
}

export function generateVietnamesePhone(): string {
  const prefixes = [
    '090',
    '091',
    '094',
    '083',
    '084',
    '085',
    '081',
    '082',
    '032',
    '033',
    '034',
    '035',
    '036',
    '037',
    '038',
    '039',
  ];
  const prefix = getRandomElement(prefixes);
  const suffix = Math.floor(Math.random() * 10000000)
    .toString()
    .padStart(7, '0');
  return prefix + suffix;
}

export function generateStreetAddress(): string {
  const number = Math.floor(Math.random() * 999) + 1;
  const streetPrefix = getRandomElement(STREET_PREFIXES);
  const streetSuffix = getRandomElement(STREET_SUFFIXES);
  return `${number} ${streetPrefix} ${streetSuffix}`;
}
